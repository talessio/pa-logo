package PA.Logo.Model;

public class CoordinateIn2D implements Coordinate {
    double x;
    double y;

    public void setX(double x) {
        this.x = x;
    }

    public double getX() {
        return x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getY() {
        return y;
    }
}
